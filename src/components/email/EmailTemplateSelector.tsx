// ---------------------------------------------------------------------------
// Props
// ---------------------------------------------------------------------------

export interface EmailTemplateSelectorProps {
  onSelect: (templateType: string, subject: string, body: string) => void;
  data?: EmailTemplateData;
}

// ---------------------------------------------------------------------------
// Component
// ---------------------------------------------------------------------------

export function EmailTemplateSelector({
  onSelect,
  data = {},
}: EmailTemplateSelectorProps) {
  function handleClick(templateValue: string) {
    const subject = getEmailSubject(templateValue, data);
    const body = getEmailBody(templateValue, data);
    onSelect(templateValue, subject, body);
  }

  return (
    <div className="grid grid-cols-2 gap-3 sm:grid-cols-4">
      {EMAIL_TEMPLATES.map((template) => (
        <button
          key={template.value}
          type="button"
          onClick={() => handleClick(template.value)}
          className="flex flex-col items-center gap-2 rounded-lg border border-primary-200 bg-white p-4 text-center transition-colors hover:border-accent hover:bg-primary-50"
        >
          <span className="text-primary-600">
            {TEMPLATE_ICONS[template.value] ?? TEMPLATE_ICONS.custom}
          </span>
          <span className="text-xs font-medium text-primary-700">
            {template.label}
          </span>
        </button>
      ))}
    </div>
  );
}
